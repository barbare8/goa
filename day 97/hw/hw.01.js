// for…in და for…of ორივე ლუპია, მაგრამ ისინი განსხვავებულ რამეს „უყურებენ“.

// for…in გეუბნება:
//  „რა სახელები (key-ები) აქვს ამ ობიექტს?“
// ანუ ის გადადის ობიექტის ველებზე (სახელი, ასაკი, ქალაქი და ა.შ.).
// თუ მასივზე გამოიყენებ, დაგიბრუნებს ინდექსებს (0, 1, 2…), ამიტომ მასივებისთვის არ არის ძალიან კარგი.

// for…of გეუბნება:
//  „რა მნიშვნელობები ინახება აქ?“
// ის პირდაპირ გადადის მნიშვნელობებზე.
// ამიტომ ძალიან მოსახერხებელია მასივებისთვის, სტრინგებისთვის და მსგავსი მონაცემებისთვის.

//  მოკლედ რომ ვთქვათ:

// for…in — გადის სახელებზე / ინდექსებზე

// for…of — გადის მნიშვნელობებზე

// თუ ამას გამოცდისთვის ამბობ, ერთი წინადადება ასე ივარგებს:
//  for…in გამოიყენება ობიექტის key-ებზე გადასავლელად, ხოლო for…of — მასივისა და სხვა iterable მონაცემების მნიშვნელობებზე.
const word = "Hello";

for (let char of word) {
  console.log(char); // H e l l o
}
