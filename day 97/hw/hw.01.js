//  1 Default Function Creating (ჩვეულებრივი ფუნქცია)
function square1(x) {
  return x * x;
}

//  2Function Expression (ფუნქცია ცვლადში)
const square2 = function (x) {
  return x * x;
};

// 3 Arrow Function (ისრიანი ფუნქცია)
const square3 = (x) => x * x;

// გამოყენების მაგალითები
console.log(square1(4)); // 16
console.log(square2(5)); // 25
console.log(square3(6)); // 36